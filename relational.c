/*
Relational operators
   less than:  <
   greater than : >
   less than or equal : <=
   greater than equal : >=
   not equal:    !=
   both equal or not :  == 
   */
  #include<stdio.h>
  #include<stdbool.h>
  int main()
  {
    int a=6;
    int b=4;
      printf("A==B = %d\n", a==b);
      printf("A<B = %d\n",a<b);
      printf("A>B = %d\n",a>b);
      printf("A!=B = %d\n",a!=b);
      int c = 7, d = 8;
      printf("c<=d = %d\n",c<=d);
      printf("c>=d = %d",c>=d);
  }
